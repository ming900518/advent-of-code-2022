def parseInput(input: String): List[ArrayBuffer[String]] = {
  val result = List(
    ArrayBuffer[String](),
    ArrayBuffer[String](),
    ArrayBuffer[String](),
    ArrayBuffer[String](),
    ArrayBuffer[String](),
    ArrayBuffer[String](),
    ArrayBuffer[String](),
    ArrayBuffer[String](),
    ArrayBuffer[String]()
  );
  input
    .split("\n")
    .dropRight(1)
    .map((line) => {
      line
        .grouped(4)
        .zip(result)
        .foreach((value, col) => {
          val extracted = value.substring(1, 2)
          if extracted != " " then {
            col += value.substring(1, 2)
          }
        });
    });
  result
}

def parseInstruction(instruction: String): List[(Int, Int, Int)] = {
  instruction
    .split("\n")
    .map((line) => {
      line
        .split(" ")
        .map((splited) => splited.toIntOption)
        .filter((value) => value.isDefined)
        .grouped(3)
        .map((value) =>
          (value.apply(0).get, value.apply(1).get, value.apply(2).get)
        )
        .next()
    })
    .toList
}

